int blinkStage[264];
int hasToCopyBlinkStage = 1;

int stageStartOffsetX = 0;
int stageStartOffsetY = 0;

int lineHasToBlink = 0;

const float blinkLineTimer = 0.2f;
float timeToBlinkLine = 0.2f;

const int maxNumBlinks = 6;
int currentNumBlinks = 6;

extern const Color colorTypes[];

extern char scoreTxt[12];
extern int score;

extern float moveTetrominoDownTimer;

extern int isTetrominoActive;

void ResetLines(int startLineY)
{
    for (int y = startLineY; y >= 0; y--)
    {
        for (int x = 1; x < STAGE_WIDTH - 1; x++)
        {
            const int offset = y * STAGE_WIDTH + x;
            const int offset_below = (y + 1) * STAGE_WIDTH + x;

            if (stage[offset_below] == 0 && stage[offset] > 0)
            {
                stage[offset_below] = stage[offset];
                stage[offset] = 0;
            }
        }
    }
}

int HasStageLinesToDelete()
{
    for (int y = 0; y < STAGE_HEIGHT - 1; y++)
    {
        int checkLine = 1;

        for (int x = 1; x < STAGE_WIDTH - 1; x++)
        {
            const int offset = y * STAGE_WIDTH + x;

            if (stage[offset] == 0)
            {
                checkLine = 0;
                break;
            }
        }

        if (checkLine)
        {
            return 1;
        }
    }

    return 0;
}

int HasBlinkStageLinesToDelete()
{
    for (int y = 0; y < STAGE_HEIGHT - 1; y++)
    {
        int checkLine = 1;

        for (int x = 1; x < STAGE_WIDTH - 1; x++)
        {
            const int offset = y * STAGE_WIDTH + x;

            if (blinkStage[offset] == 0)
            {
                checkLine = 0;
                break;
            }
        }

        if (checkLine)
        {
            return 1;
        }
    }

    return 0;
}

void BlinkLines()
{
    if (hasToCopyBlinkStage)
    {
        memcpy_s(blinkStage, sizeof(blinkStage), stage, sizeof(stage));
        hasToCopyBlinkStage = 0;
    }

    for (int y = 0; y < STAGE_HEIGHT - 1; y++)
    {
        int checkLine = 1;

        for (int x = 1; x < STAGE_WIDTH - 1; x++)
        {
            const int offset = y * STAGE_WIDTH + x;

            if (blinkStage[offset] == 0)
            {
                checkLine = 0;
                break;
            }
        }

        if (checkLine)
        {
            const int offset = y * STAGE_WIDTH + 1;

            if (lineHasToBlink)
            {
                for (int i = offset, x = 0; x < STAGE_WIDTH - 2; i++, x++)
                {
                    stage[i] = blinkStage[i];
                }
            }

            else
            {
                memset(stage + offset, 0, (STAGE_WIDTH - 2) * sizeof(int));
            }
        }
    }

    lineHasToBlink = !lineHasToBlink;
    timeToBlinkLine = blinkLineTimer;
    currentNumBlinks--;
}

void DeleteLines()
{
    for (int y = 0; y < STAGE_HEIGHT - 1; y++)
    {
        int checkLine = 1;

        for (int x = 1; x < STAGE_WIDTH - 1; x++)
        {
            const int offset = y * STAGE_WIDTH + x;

            if (stage[offset] == 0)
            {
                checkLine = 0;
                break;
            }
        }

        if (checkLine)
        {
            const int offset = y * STAGE_WIDTH + 1;
            memset(stage + offset, 0, (STAGE_WIDTH - 2) * sizeof(int));

            score = atoi(scoreTxt);

            if (score + 10 >= 0) // overflow check
                score += 10;

            else
                score = _I32_MAX;

            _itoa_s(score, scoreTxt, sizeof(scoreTxt), 10);

            if (score % 50 == 0)
            {
                if (moveTetrominoDownTimer > 0.2f)
                {
                    moveTetrominoDownTimer -= 0.1f;
                }
            }

            ResetLines(y);
        }
    }

    memset(blinkStage, 0, sizeof(blinkStage));
    hasToCopyBlinkStage = 1;
    lineHasToBlink = 0;
    currentNumBlinks = maxNumBlinks;
    timeToBlinkLine = blinkLineTimer;

    isTetrominoActive = 1;
}

void DrawStage()
{
    for (int y = 0; y < STAGE_HEIGHT; y++)
    {
        for (int x = 0; x < STAGE_WIDTH; x++)
        {
            const int offset = y * STAGE_WIDTH + x;
            const int color = stage[offset];

            if (stage[offset] != 0)
            {
                DrawRectangle(x * TILE_SIZE + stageStartOffsetX, y * TILE_SIZE + stageStartOffsetY, TILE_SIZE, TILE_SIZE, colorTypes[color - 1]);
            }

            DrawRectangleLines(x * TILE_SIZE + stageStartOffsetX, y * TILE_SIZE + stageStartOffsetY, TILE_SIZE, TILE_SIZE, BLACK);
        }
    }
}